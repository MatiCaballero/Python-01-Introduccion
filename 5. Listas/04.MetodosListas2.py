lista = [1, 2, 3, 4, 5, 6, 7, 8, 1, 4, 2, 1, 1, 3, 4, 5, 5, 6, 7]

# .count = Cuenta la cantidad de veces que aparece un elemento en la lista

print(lista.count(1))

# .index = recibe un parametro y busca en la lista, devuelve la posicion en la que se encuentra

# Devuelve el espacio en donde encuentra el primer numero.
print(lista.index(1))

# .sort = Ordena la lista de menor a mayor

lista.sort()
