lista = ['Matias', 10, 3, 7, 888, 'Mercado Libre', 1.309]

print(lista[5])

# Nos muestra del espacio 0 al 5
print(lista[0: 5])

# Nos muestra del principio la limite (2)
print(lista[: 2])

# Nos muestra del espacio especificado, hasta el final.
print(lista[2:])

# En negativo, muestra del ultimo al primer dato
print (lista[-1])
