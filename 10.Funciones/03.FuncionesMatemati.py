
import math  # Importo las funciones math
import random #Importo las funciones de random

# .pow(*,*) Eleva un numero (potencia)
print(math.pow(4, 3))

#.sqrt = Raiz cuadrada de un numero
print(math.sqrt(64))

#.isqrt = Raiz cuadrada, numero entero
print (math.isqrt(25))

#.sin = Seno
print (math.sin(90))
#.cos = Coseno
print (math.cos(180))
#.tan = Tangente
print (math.tan(45))

#.factorial = Multiplica un 1*2*3*4 ... n
print (math.factorial(6))

#.random = metodos aleatorios
print (random.randint(1,100)) #Numero al azar de 1 al 100

